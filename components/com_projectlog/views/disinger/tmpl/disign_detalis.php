<script type="text/javascript">

    JQ(document).ready(function() {
        JQ('#buildTable').bind('click', function(e) {
            e.preventDefault();

            JQ('#tablePlaceholder').empty()
                .buildTable();
        });

        JQ('#runPlugin').bind('click', function(e) {
            e.preventDefault();

            JQ('#tablePlaceholder table').fixedHeaderTable({ height: '400', altClass: 'odd', themeClass: 'fancyDarkTable' });
        });
    });

    JQ.fn.extend({
        buildTable: function() {
            var defaults = {
                width: '100%',
                height: 400,
                rows: 100,
                columns: 5,
                borderWidth: 1,
                borderColor: '#000000',
                padding: 5
            };

            var $self 		= JQ(this),
                self  		= this,
                rows  		= JQ('#rows').val(),
                columns 	= JQ('#columns').val(),
                borderWidth = JQ('#borderWidth').val(),
                borderColor = JQ('#borderColor').val(),
                padding 	= JQ('#padding').val(),
                $thead,
                $tbody,
                $currentRow,
                $styles,
                options		= {};

            if ( padding != '' ) {
                options.padding = padding;
            }

            options = JQ.extend({}, defaults, options);

            JQself.append('<table><thead></thead><tbody></tbody></table>');

            JQthead = $self.find('thead');
            JQthead.append('<tr></tr>');
            for ( var column = 1; column <= columns; column++ ) {
                JQthead.find('tr')
                    .append('<th>Column '+column+'</th>');
            }

            JQtbody = $self.find('tbody');
            for ( var row = 1; row <= rows; row++ ) {
                $currentRow = JQ('<tr></tr>').appendTo($tbody);

                for ( var column = 1; column <= columns; column++ ) {
                    JQcurrentRow.append('<td>Column '+column+'</td>');
                }
            }

            /*
                    var paddingStyle = "padding: " + options.padding + "px !important;";

                    $styles = $('<style></style>').appendTo('.styles');

                    $styles.html('th, td { '+paddingStyle+' }');
            */
        }
    });


</script>